#include "libft.h"

int		ft_isprint(int c)
{
	if (c >= 32 && c <= 39)
		return (1);
	else if
		(c >= 40 && c <= 47)
		return (1);
	else if
		(c >= 48 && c <= 126)
		return (1);
	else
		return (0);
}
