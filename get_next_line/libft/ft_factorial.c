#include "libft.h"

int		ft_factorial(int nb)
{
	int		nb1;

	nb1 = nb - 1;
	if (nb == 0 || nb > 12)
		return (1);
	while (nb1 > 0)
	{
		nb *= nb1;
		nb1--;
	}
	return (nb);
}
